/*
 * For your reference:
 *
 * SinglyLinkedListNode {
 *     int data;
 *     SinglyLinkedListNode* next;
 * };
 *
 */

SinglyLinkedListNode* insertNodeAtPosition(SinglyLinkedListNode* llist, int data, int position) {
    
    SinglyLinkedListNode* current;
    SinglyLinkedListNode* newNode;
    current = llist;

    for (int i = 0; i <= position - 1; i++) { //We iterate until the position BEFORE the desired position
        
        if (i == position - 1) {
            
            newNode = new SinglyLinkedListNode(data); //once we are right before the desired position, create a new node with data
            newNode->next = current->next; //newNode points to the node that's currently in the desired position
            current->next = newNode; //newNode is now in the desired position by changing where current->next is pointing to newNode (current is right before the desired position)
            
        } else {
            
            current = current->next;
            
        }  
        
    }
    
    return llist;

}

